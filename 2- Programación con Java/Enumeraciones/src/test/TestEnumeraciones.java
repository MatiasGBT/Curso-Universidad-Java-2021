package test;

import enumeración.*;

public class TestEnumeraciones {
	public static void main(String[] args) {
		System.out.println(Dias.LUNES);
		indicarDiaSemana(Dias.LUNES);
		
		System.out.println("Continente no. 4: " + Continentes.AMERICA);
		System.out.println("Número de países del 4to continente: " + Continentes.AMERICA.getPaises());
	}
	
	//Si el método no se define static salta error al usar enumeraciones
	private static void indicarDiaSemana(Dias dias) {
		switch (dias) {
		case LUNES:
			System.out.println("Primer día de la semana");
			break;
		case MARTES:
			System.out.println("Segundo día de la semana");
			break;
		case MIERCOLES:
			System.out.println("Tercer día de la semana");
			break;
		case JUEVES:
			System.out.println("Cuarto día de la semana");
			break;
		case VIERNES:
			System.out.println("Quinto día de la semana");
			break;
		case SABADO:
			System.out.println("Sexto día de la semana");
			break;
		case DOMINGO:
			System.out.println("Septimo día de la semana");
			break;
		default:
			break;
		}
	}
}
